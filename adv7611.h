void adv7611_init_reg() {
  int i = 0;
  byte addr = 0x00;
  byte reg  = 0x00;
  byte val  = 0x00;

  while (true) {
    // Stop at 0xFF,0xFF,0xFF,0xFF
    if (adv_map[i] == 0xFF && adv_map[i+1] == 0xFF && adv_map[i+2] == 0xFF && adv_map[i+3] == 0xFF) {
      break;
    }

    // Update device address
    if (adv_map[i] == 0xFF && adv_map[i+1] == 0xFF && adv_map[i+2]) {
      i+=3;
      addr = adv_map[i++];
    }

    reg = adv_map[i++];
    val = adv_map[i++];
    
    Wire.beginTransmission(addr);
    Wire.write(reg);
    Wire.write(val);
    Wire.endTransmission();
  }
}
